--부서번호가 60,70,80,90 인 사원들의 EMPLOYEE_ID, FIRST_NAME, HIRE_DATE, JOB_ID 조회
--단 JOB_ID가 IT_PROG인 사원의 경우 프로그래머로 변경하여 출력한다.
